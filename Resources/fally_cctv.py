"""# Put video to test"""

import os
from google.colab import files
from ultralytics import YOLO

# 1. Tạo khu vực upload file video từ máy tính của bạn
print("Vui lòng chọn file video (.mp4, .avi, .mov) cần nhận diện:")
uploaded = files.upload()

# 2. Tự động lấy tên file video vừa được tải lên thành công
video_path = list(uploaded.keys())[0]
print(f"\n Đã tải lên thành công file: {video_path}")

# 3. Tải mô hình YOLOv8 bạn vừa huấn luyện xong
model = YOLO('/content/runs/detect/train-2/weights/best.pt')

# 4. Tiến hành chạy nhận diện (Predict) trên video vừa tải
print("\n Đang tiến hành xử lý nhận diện hành động trên video...")
results = model.predict(
    source=video_path,
    conf=0.40,       # Ngưỡng tin cậy 40% để giảm thiểu lỗi nhận diện sai
    save=True        # Lưu lại video đã vẽ khung bounding box
)

# 5. Xác định thư mục lưu trữ video đầu ra
# YOLOv8 tự động tạo thư mục predict tăng dần (predict, predict2,...)
latest_predict_dir = model.predictor.save_dir
output_video_path = os.path.join(latest_predict_dir, video_path)

print(f"\n Hoàn tất! Video kết quả nhận diện đã được lưu tại: {output_video_path}")