    inner_folders = os.listdir(img_dir)
    print("Các thư mục bên trong thư mục ảnh:", inner_folders)

    # Định dạng lại tên cho đúng chữ hoa/chữ thường thực tế
    train_folder = 'train' if 'train' in inner_folders else ('Train' if 'Train' in inner_folders else inner_folders[0])
