def process_json_file(file_path: Path):
    try:
        with file_path.open("r", encoding="utf-8") as file:
            payload = json.load(file)
    except Exception:
        return None

    sensors = payload.get("sensors", [])
    if not sensors:
        return None

    rows = []
    for sample in sensors:
        accelerometer = sample.get("accelerometer", {})
        gyroscope = sample.get("gyroscope", {})
        rows.append(
            [
                accelerometer.get("x", np.nan),
                accelerometer.get("y", np.nan),
                accelerometer.get("z", np.nan),
                gyroscope.get("x", np.nan),
                gyroscope.get("y", np.nan),
                gyroscope.get("z", np.nan),
            ]
        )

    df_res = np.asarray(rows, dtype=np.float32)
    if df_res.size == 0:
        return None

    if np.isnan(df_res).any():
        for column_index in range(df_res.shape[1]):
            column = df_res[:, column_index]
            valid = column[~np.isnan(column)]
            fill_value = float(valid.mean()) if valid.size else 0.0
            column[np.isnan(column)] = fill_value
            df_res[:, column_index] = column

    relative_folder = file_path.parent.relative_to(DATASET_ROOT).as_posix().lower()
    label = 1 if relative_folder.startswith("fall -") else 0

    return {
        "data": df_res,
        "Label": label,
        "Activity": "fall" if label == 1 else "adl",
        "Subject": file_path.parent.name,
    }


def create_windows(
    data: np.ndarray, window_size: int = 32, overlap: int = 16
) -> np.ndarray:
    if len(data) < window_size:
        padded = np.repeat(data[-1:, :], window_size - len(data), axis=0)
        return np.asarray([np.vstack([data, padded])], dtype=np.float32)

    step = max(1, window_size - overlap)
    windows = []

    for i in range(0, len(data) - window_size + 1, step):
        windows.append(data[i : i + window_size])

    return np.asarray(windows, dtype=np.float32)


def extract_statistical_features(X_raw: np.ndarray) -> np.ndarray:
    features_list = []
    for window in X_raw:
        means = np.mean(window, axis=0)
        stds = np.std(window, axis=0)
        maxs = np.max(window, axis=0)
        mins = np.min(window, axis=0)
        smv = np.sqrt(np.sum(window[:, :3] ** 2, axis=1))
        smv_feat = [np.mean(smv), np.std(smv), np.max(smv)]

        row = np.hstack([means, stds, maxs, mins, smv_feat])
        features_list.append(row)

    return np.asarray(features_list, dtype=np.float32)


