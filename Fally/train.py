def export_to_onnx(model, num_features: int, output_dir: Path) -> None:
    try:
        from onnxmltools import convert_xgboost
        from onnxmltools.convert.common.data_types import FloatTensorType
    except Exception:
        print("Bỏ qua xuất ONNX vì thiếu onnxmltools.")
        return

    # For XGBoost conversion we need to specify target_opset for onnxconverter_common fallback
    initial_type = [("float_input", FloatTensorType([None, num_features]))]

    try:
        onnx_model = convert_xgboost(model, initial_types=initial_type, target_opset=9)
    except Exception:
        # Sometimes convert_xgboost internally expects the onnxconverter_common type despite what shape_calculator says
        try:
            from onnxconverter_common.data_types import (
                FloatTensorType as FloatTensorTypeCommon,
            )

            initial_type_common = [
                ("float_input", FloatTensorTypeCommon([None, num_features]))
            ]
            onnx_model = convert_xgboost(
                model, initial_types=initial_type_common, target_opset=9
            )
        except Exception as e:
            print(f"Lỗi xuất ONNX (cả 2 kiểu TensorType đều thất bại): {e}")
            return

    onnx_filename = output_dir / "fall_model.onnx"
    with onnx_filename.open("wb") as file:
        file.write(onnx_model.SerializeToString())

    print(f"Mô hình ONNX đã được lưu: {onnx_filename}")

    try:
        import onnxruntime as ort

        sess = ort.InferenceSession(str(onnx_filename))
        dummy_input = np.random.randn(1, num_features).astype(np.float32)
        input_name = sess.get_inputs()[0].name
        label_name = sess.get_outputs()[0].name
        prob_name = sess.get_outputs()[1].name
        prediction = sess.run([label_name, prob_name], {input_name: dummy_input})
        print("--- KIỂM TRA MODEL ONNX ---")
        print(f"Nhãn dự đoán giả lập: {prediction[0]}")
        print(f"Xác suất chi tiết: {prediction[1]}")
    except Exception:
        print("Đã lưu ONNX nhưng bỏ qua bước kiểm tra vì thiếu onnxruntime.")


